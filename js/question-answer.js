$(".question1>button").click(function () {
    $('.question1').toggleClass("question-answer__answer_visible");
    if ($('.question1>.question-answer__answer').is(':visible')) {
        $('.question1>.question-answer__answer').slideUp( "slow");
    }
    else {
        $('.question1>.question-answer__answer').slideDown( "slow");
    }
});
